import mongoose from "mongoose";

const ReviewSchema = new mongoose.Schema(
  {
    movieId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Movie",
      required: true,
    },
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    comment: {
      type: String,
      required: true,
    },
    rating: {
      type: Number,
      required: true,
      min: 1,
      max: 10,
    },
    likes: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);

export const ReviewModel = mongoose.model("Review", ReviewSchema);
